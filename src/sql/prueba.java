    public prueba() {
        initComponents();
        try {
            table1.setModel(modelo);
            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            java.sql.Connection con = conn.getConexion();

            String sql = "SELECT alumnos.nombre_usuario,alumnos.edad,carreras.carrera,grupo.grupo,turno.turno FROM alumnos INNER JOIN carreras ON alumnos.id_carrera = carreras.id_carrera INNER JOIN grupo on alumnos.id_grupo = grupo.id_grupo INNER JOIN turno on alumnos.id_turno = turno.id_turno";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();

            modelo.addColumn("Nombre");
            modelo.addColumn("Edad");
            modelo.addColumn("Carrera");
            modelo.addColumn("Grupo");
            modelo.addColumn("Turno");
            while (rs.next()) {
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
    }

